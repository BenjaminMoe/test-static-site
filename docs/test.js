// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Function to subtract two numbers
function subtract(a, b) {
    return a - b;
}

// Function to multiply two numbers
function multiply(a, b) {
    return a * b;
}

// Function to divide two numbers
function divide(a, b) {
    return a / b;
}

// FFT function
function fft(a, b) {
    return a * b;
}

// Export the functions
module.exports = {
    add,
    subtract,
    multiply,
    divide,
    fft
};

